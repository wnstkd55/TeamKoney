package kr.co.city;

public class City {
	private String c_name;
	private double c_ny;
	private double c_nx;
	private String c_pic;
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public double getC_ny() {
		return c_ny;
	}
	public void setC_ny(double c_ny) {
		this.c_ny = c_ny;
	}
	public double getC_nx() {
		return c_nx;
	}
	public void setC_nx(double c_nx) {
		this.c_nx = c_nx;
	}
	public String getC_pic() {
		return c_pic;
	}
	public void setC_pic(String c_pic) {
		this.c_pic = c_pic;
	}
	
	
}
